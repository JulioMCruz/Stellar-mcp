export function registerScaffoldTools(server: McpServer): void {
  server.tool(
    "stellar_soroban_scaffold_contract",
    "Create a Rust Soroban smart contract scaffold with tests and Stellar CLI build/deploy/bindings commands.",
    {
      outputDir: outputDirSchema,
      contractName: nameSchema.default("hello_world").describe("Contract/crate name to scaffold."),
      overwrite: z.boolean().default(false).describe("Overwrite existing scaffold files if they already exist.")
    },
    async ({ outputDir, contractName, overwrite }) => {
      try {
        const files = sorobanContractFiles(contractName);
        const writtenFiles = writeScaffoldFiles(outputDir, files, overwrite);
        return {
          content: [{
            type: "text",
            text: JSON.stringify({
              status: "created",
              scaffold: "soroban-contract",
              contractName: toSnakeName(contractName),
              files: writtenFiles,
              nextSteps: [
                "rustup target add wasm32v1-none",
                "cargo test",
                "stellar contract build",
                "stellar contract deploy --wasm target/wasm32v1-none/release/<contract>.wasm --source <key> --network testnet",
                "stellar contract bindings typescript --contract-id <CONTRACT_ID> --output-dir ./packages/<contract> --overwrite"
              ]
            }, null, 2)
          }]
        };
      } catch (error) {
        return {
          isError: true,
          content: [{ type: "text", text: redactSensitiveText(error instanceof Error ? error.message : String(error)) }]
        };
      }
    }
  );

  server.tool(
    "stellar_nextjs_wallet_scaffold",
    "Create Next.js wallet components for Stellar/Freighter plus env and contract binding notes.",
    {
      outputDir: outputDirSchema,
      appName: nameSchema.default("stellar_dapp").describe("App name used in generated notes."),
      overwrite: z.boolean().default(false).describe("Overwrite existing scaffold files if they already exist.")
    },
    async ({ outputDir, appName, overwrite }) => {
      try {
        const files = nextWalletFiles(appName);
        const writtenFiles = writeScaffoldFiles(outputDir, files, overwrite);
        return {
          content: [{
            type: "text",
            text: JSON.stringify({
              status: "created",
              scaffold: "nextjs-stellar-wallet",
              files: writtenFiles,
              dependencies: ["@stellar/freighter-api", "@stellar/stellar-sdk"],
              nextSteps: [
                "npm install @stellar/freighter-api @stellar/stellar-sdk",
                "use next dev --experimental-https for local Freighter testing",
                "copy .env.local.example to .env.local",
                "generate Stellar CLI TypeScript bindings after deploying a Soroban contract"
              ]
            }, null, 2)
          }]
        };
      } catch (error) {
        return {
          isError: true,
          content: [{ type: "text", text: redactSensitiveText(error instanceof Error ? error.message : String(error)) }]
        };
      }
    }
  );
}
