const outputDirSchema = z
  .string()
  .trim()
  .min(1)
  .describe("Directory where scaffold files should be written.")
  .refine((value) => {
    const normalized = resolve(value).replace(/\\/g, "/");
    if (normalized === "/" || normalized === "/root" || normalized === "/etc") return false;
    const restrictedPrefixes = ["/etc/", "/proc/", "/sys/", "/dev/", "/root/", "/boot/", "/var/log/", "/run/"];
    return !restrictedPrefixes.some((prefix) => normalized.startsWith(prefix));
  }, {
    message: "Output directory targets a restricted system path."
  });

const nameSchema = z
  .string()
  .trim()
  .min(1)
  .max(64)
  .regex(/^[A-Za-z][A-Za-z0-9_-]*$/, "Name must start with a letter and contain only letters, numbers, '_' or '-'.");

type ScaffoldFile = {
  path: string;
  content: string;
};

function toSnakeName(name: string): string {
  return name
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/[-\s]+/g, "_")
    .replace(/[^A-Za-z0-9_]/g, "_")
    .replace(/_+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toLowerCase() || "hello_world";
}

function toPascalName(name: string): string {
  return toSnakeName(name)
    .split("_")
    .filter(Boolean)
    .map((part) => part.slice(0, 1).toUpperCase() + part.slice(1))
    .join("") || "HelloWorld";
}

function toKebabName(name: string): string {
  return toSnakeName(name).replace(/_/g, "-");
}

export function writeScaffoldFiles(outputDir: string, files: ScaffoldFile[], overwrite: boolean): string[] {
  const root = resolve(outputDir);
  const written: string[] = [];

  for (const file of files) {
    const absolutePath = resolve(root, file.path);
    if (!absolutePath.startsWith(`${root}/`) && absolutePath !== root) {
      throw new Error(`Refusing to write outside outputDir: ${file.path}`);
    }
    if (!overwrite && existsSync(absolutePath)) {
      throw new Error(`Refusing to overwrite existing file: ${absolutePath}`);
    }
    mkdirSync(dirname(absolutePath), { recursive: true });
    writeFileSync(absolutePath, file.content, "utf8");
    written.push(absolutePath);
  }

  return written;
}

